public class SimpleIfElseLadder {
    public static void main(String[] args) {
        String name = "onkar";
        
        if (name=="onkar") {
            System.out.println("Name is Onkar");
        } else if (name=="Amit") {
            System.out.println("Name is Amit");
        } else {
            System.out.println("Name is Rajesh");
        }
    }
}
