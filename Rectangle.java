class Rectangle {
    public static void main(String[] args) {
        int l = 10;
        int b = 5;
        
        int area = l * b;
        int perimeter = 2 * (l + b);
        
        System.out.println("The area of rectangle with length " + l + " and breadth " + b + " is " + area);
        System.out.println("The perimeter of rectangle with length " + l + " and breadth " + b + " is " + perimeter);
    }
}
